package Semana_007.ex003;

public class Professor {
    private String nome;
    private String grr;
    public Professor(String nome, String grr) {
        this.nome = nome;
        this.grr = grr;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getGrr() {
        return grr;
    }
    public void setGrr(String grr) {
        this.grr = grr;
    }
    
}
